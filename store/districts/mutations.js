export default {
    setDistricts(state, districts) {
        state.districts = districts;
    },
    setTotalDistrict(state, totalDistrict) {
        state.totalDistrict = totalDistrict;
    },
    setSelectedDistrict(state, district) {
        state.selectedDistrict = district;
    },
    setDistrictFile(state, districtFile) {
        state.districtFile = districtFile;
    }
}